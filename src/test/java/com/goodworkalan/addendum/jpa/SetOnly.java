/**
 * A class that has a setter only.
 *
 * @author Alan Gutierrez
 */
public class SetOnly {
    /** The value. */
    public int value;

    /**
     * Set the value.
     * 
     * @param value
     *            The value.
     */
    public void setValue(int value) {
        this.value = value;
    }
}
